# Программа для вычисления общего сопротивления электрической цепи
R1 = float(input("Введите сопротивление R1: "))
R2 = float(input("Введите сопротивление R2: "))
R3 = float(input("Введите сопротивление R3: "))
R4 = float(input("Введите сопротивление R4: "))
R5 = float(input("Введите сопротивление R5: "))

R_total = 1 / (1/R1 + 1/R2 + 1/R3 + 1/R4 + 1/R5)

print(f"Общее сопротивление цепи: {R_total:.2f}")
